// Command neongen emits the arm64 NEON kernel block_arm64.s.
//
// The kernel hashes four independent 32-byte messages per iteration. The four
// messages are loaded and transposed so that vector lane k of register Vw holds
// message word w of message k; the two RIPEMD-160 pipelines then run with one
// 32-bit lane per message. Constants live in registers for the whole call, each
// round's additive constant is materialized once per round (not per step), and
// the all-zero schedule words of the single-block padding are skipped entirely.
//
// Register allocation:
//
//	V0..V7   message schedule words x0..x7 (reloaded and transposed per block)
//	V8       0x00000080 (schedule word x8, the padding "1" bit)
//	V9       current round's left additive constant
//	V10      current round's right additive constant
//	V14      0x00000100 (schedule word x14, the 256-bit message length)
//	V31      0xffffffff (mask for the bitwise-NOT in f3/f5)
//	V16..V30 the two pipelines' state plus the per-step scratch (rotation pool)
//	V0..V4   reused for the finalized digest words after the rounds complete
//
// The generator is the single source of truth for block_arm64.s; do not edit
// the assembly by hand (see CONTRIBUTING.md).
package main

import (
	"bytes"
	"fmt"
	"os"
	"path/filepath"
	"strings"
)

// modulePath is the import path of the module that owns the generated file. It
// is used to verify the resolved repository root before writing, so a wrong
// working directory fails loudly instead of silently writing outside the repo.
const modulePath = "github.com/davidzita/ripemd160mb"

